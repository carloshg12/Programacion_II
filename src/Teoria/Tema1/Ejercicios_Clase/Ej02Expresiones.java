package Teoria.Tema1.Ejercicios_Clase;

public class Ej02Expresiones {

	public static void main(String[] args) {
		System.out.print("¿Cuál es el resultado de evaluar 10 / 2 ? ");
		System.out.println(10 / 2);
		
		System.out.print("¿Cuál es el resultado de evaluar 10.0 / 2 ? ");
		System.out.println(10.0 / 2);
		
		System.out.print("¿Cuál es el resultado de evaluar 10 / 2f ? ");
		System.out.println(10 / 2f);
		
		System.out.print("¿Cuál es el resultado de evaluar (30 / 3) / 2 ? ");
		System.out.println((30 / 3) / 2);
		
		System.out.print("¿Cuál es el resultado de evaluar 30 / (3 / 2) ? ");
		System.out.println(30 / (3 / 2));
		
		System.out.print("¿Cuál es el resultado de evaluar 30 / 3 / 2 ? ");
		System.out.println(30 / 3 / 2);
		
		System.out.print("¿Cuál es el resultado de evaluar 41 / 10 ? ");
		System.out.println(41 / 10);
		
		System.out.print("¿Cuál es el resultado de evaluar 49 / 10 ? ");
		System.out.println(49 / 10);
		
		System.out.print("¿Cuál es el resultado de evaluar (double) 9 / 2 ? ");
		System.out.println((double) 9 / 2);
		
		System.out.print("¿Cuál es el resultado de evaluar (double) (9 / 2) ? ");
		System.out.println((double) (9 / 2));
		
		System.out.print("¿Cuál es el resultado de evaluar 5 > 10 ? ");
		System.out.println(5 > 10);
		
		System.out.print("¿Cuál es el resultado de evaluar 5d > 10 ? ");
		System.out.println(5d > 10);
		
	}

}
